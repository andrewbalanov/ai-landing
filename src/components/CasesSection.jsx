function CasesSection() {
  const ref = useRef(null)

  useEffect(() => {
    const observer = new IntersectionObserver(entries => {
      entries.forEach(e => {
        if (e.isIntersecting) {
          e.target.style.opacity = '1'
          e.target.style.transform = 'translateY(0)'
        }
      })
    }, { threshold: 0.08 })

    const els = ref.current?.querySelectorAll('.case-card')
    els?.forEach(el => {
      el.style.opacity = '0'
      el.style.transform = 'translateY(20px)'
      el.style.transition = 'opacity .5s ease, transform .5s ease'
      observer.observe(el)
    })
    return () => observer.disconnect()
  }, [])

  return (
    <div id="cases" className="cases-section">
      <section ref={ref}>
        <div className="sec-header">
          <h2 className="sec-h2">Наши AI-агенты уже помогают застройщикам</h2>
          <p className="cases-subhead">10 острых проблем отрасли, которые стоит автоматизировать прямо сейчас</p>
        </div>
        <div className="cases-grid">
          {cases.map((c, i) => (
            <div className="case-card" key={i}>
              <div className="case-icon">{c.icon}</div>
              <div className="case-body">
                <h4>{c.title}</h4>
                <p>{c.text}</p>
                <div className="case-results">
                  {c.results.map((r, j) => <div className="case-result" key={j}>{r}</div>)}
                </div>
              </div>
            </div>
          ))}
        </div>
      </section>
    </div>
  )
}

export default CasesSection
